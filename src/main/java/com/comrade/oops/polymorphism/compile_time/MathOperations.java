package com.comrade.oops.polymorphism.compile_time;

public class MathOperations {
    public int add(int inputOne,int inputTwo){
        return inputOne+inputTwo;
    }

    public int add(int inputOne,int inputTwo,int inputThree){
        return inputOne+inputTwo+inputThree;
    }

    public double add(double inputOne,double inputTwo){
        return inputOne+inputTwo;
    }
}
